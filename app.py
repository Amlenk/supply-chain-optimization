    ax2.tick_params(colors='#9ca3af', labelsize=9)
    ax2.xaxis.set_major_locator(plt.MaxNLocator(8))
    ax2.set_ylabel("Units On-Hand", color="#9ca3af", fontsize=10)
    ax2.grid(True, color="#1e293b", linestyle=':', alpha=0.5)
    
    leg2 = ax2.legend(facecolor='#090d16', edgecolor='none')
    for text in leg2.get_texts():
        text.set_color('#f3f4f6')
        
    plt.xticks(rotation=30)
    plt.tight_layout()
    st.pyplot(fig2)

