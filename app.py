# Run complete simulation metrics for all SKUs dynamically to update KPI metrics
all_sim_records = []
total_baseline_cost = 0.0
total_ml_cost = 0.0
total_stockouts_baseline = 0
total_stockouts_ml = 0

for _, row in meta_df.iterrows():
    sku = row["StockCode"]
    sku_m = metrics_df[metrics_df["StockCode"] == sku].iloc[0]
    sku_p = preds_df[preds_df["StockCode"] == sku].copy().sort_values("Date")
    
    # Run rapid standard daily simulator for statistics
    actuals = sku_p["Demand"].values
    predictions = sku_p["Predicted"].values
    
    lt = max(1, int(row["LeadTime"]) + lt_modifier)
    hc = max(0.001, float(row["HoldingCostDaily"]) * (1 + hc_modifier/100))
    pen = float(row["StockoutPenalty"])
    unc = float(sku_m["Uncertainty"])
    
    # Calculate safety stocks
    std_hist = actuals.std()
    b_ss = 1.5 * std_hist * np.sqrt(lt)
    m_ss = z_score * unc * np.sqrt(lt)
    
    # Fast simulator logic
    def run_fast_sim(policy):
        inv = 100
        hold = 0.0
        stock = 0.0
        scount = 0
        pending = []
        
        b_rop = (actuals.mean() * lt) + b_ss
        b_qty = max(50, actuals.mean() * 14)
        
        for d in range(len(actuals)):
            # Arrive orders
            inv += sum(o["qty"] for o in pending if o["eta"] == d)
            pending = [o for o in pending if o["eta"] != d]
            
            dem = actuals[d]
            if inv >= dem:
                inv -= dem
            else:
                unf = dem - inv
                inv = 0
                scount += 1
                stock += unf * pen
                
            hold += inv * hc
            eff = inv + sum(o["qty"] for o in pending)
            
            if policy == "baseline":
                if eff <= b_rop:
                    pending.append({"eta": d + lt, "qty": b_qty})
            else:
                m_rop = (predictions[d] * lt) + m_ss
                m_qty = max(50, predictions[d] * 14)
                if eff <= m_rop:
                    pending.append({"eta": d + lt, "qty": m_qty})
                    
        return hold, stock, scount, inv
        
    b_h, b_s, b_c, b_final_inv = run_fast_sim("baseline")
    m_h, m_s, m_c, m_final_inv = run_fast_sim("ml")
    
    total_baseline_cost += (b_h + b_s)
    total_ml_cost += (m_h + m_s)
    total_stockouts_baseline += b_c
    total_stockouts_ml += m_c
    
    # Append for reorder decision matrix
    all_sim_records.append({
        "StockCode": sku,
        "Description": row["Description"],
        "UnitPrice": round(float(row["UnitPrice"]), 2),
        "SafetyStock": int(m_ss),
        "ROP": int((predictions[-1] * lt) + m_ss),
        "CurrentInventory": int(m_final_inv)
    })

cost_saved = total_baseline_cost - total_ml_cost
pct_saved = (cost_saved / max(1.0, total_baseline_cost)) * 100
stockout_reduction = ((total_stockouts_baseline - total_stockouts_ml) / max(1, total_stockouts_baseline)) * 100

