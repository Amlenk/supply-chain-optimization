    st.subheader("📊 Dynamic Simulated Daily Inventory Levels")
    st.write("ML-Optimized Safety Stock policy preventing stockouts vs. Historical baseline exhaustion.")
    
    # Generate simulated inventory curve
    # Re-run simulation dynamically to log daily inventory
    actuals_arr = sku_preds["Demand"].values
    forecast_arr = sku_preds["Predicted"].values
    
    std_hist = actuals_arr.std()
    b_ss = 1.5 * std_hist * np.sqrt(adjusted_lead_time)
    b_rop = (actuals_arr.mean() * adjusted_lead_time) + b_ss
    b_qty = max(50, actuals_arr.mean() * 14)
    
    # Simulated trace
    def get_inv_trace(policy):
        inv = 100
        trace = []
        pending = []
        for d in range(len(actuals_arr)):
            inv += sum(o["qty"] for o in pending if o["eta"] == d)
            pending = [o for o in pending if o["eta"] != d]
            
            dem = actuals_arr[d]
            inv = max(0, inv - dem)
            trace.append(inv)
            
            eff = inv + sum(o["qty"] for o in pending)
            if policy == "baseline":
                if eff <= b_rop:
                    pending.append({"eta": d + adjusted_lead_time, "qty": b_qty})
            else:
                m_rop = (forecast_arr[d] * adjusted_lead_time) + ml_ss
                m_qty = max(50, forecast_arr[d] * 14)
                if eff <= m_rop:
                    pending.append({"eta": d + adjusted_lead_time, "qty": m_qty})
        return trace

    baseline_trace = get_inv_trace("baseline")
    ml_trace = get_inv_trace("ml")
    
    fig2, ax2 = plt.subplots(figsize=(10, 5), facecolor='#090d16')
    ax2.set_facecolor('#0b132b')
    
    ax2.plot(dates, baseline_trace, label="Baseline Inventory", color="#ef4444", linewidth=2)
