# Dynamic z-score calculator helper
def get_z_score(service_level):
    levels = {80.0: 0.842, 85.0: 1.036, 90.0: 1.282, 95.0: 1.645, 98.0: 2.054, 99.0: 2.326, 99.9: 3.090}
    closest = min(levels.keys(), key=lambda x: abs(x - service_level))
    return levels[closest]

@st.cache_data
def load_data():
    """Loads all data files generated by pipeline and models."""
    if not (os.path.exists(PRODUCT_METADATA_PATH) and os.path.exists(FORECAST_METRICS_PATH) and os.path.exists(PREDICTIONS_PATH)):
        return None, None, None
        
    meta = pd.read_csv(PRODUCT_METADATA_PATH)
    metrics = pd.read_csv(FORECAST_METRICS_PATH)
    preds = pd.read_csv(PREDICTIONS_PATH)
    preds["Date"] = pd.to_datetime(preds["Date"])
    
    return meta, metrics, preds

